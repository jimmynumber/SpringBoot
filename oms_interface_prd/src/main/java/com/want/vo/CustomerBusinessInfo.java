package com.want.vo;

import java.io.Serializable;
/**
 * 经销商业务基本信息 对接接口
 * @author 00320558
 * 2019-12-23 11:20:00
 */
public class CustomerBusinessInfo implements Serializable{
	private static final long serialVersionUID = 6307193404515713464L;
	
	private String tenantUserid;          //该记录的ID,租户代码+userid
	private String regionalName;          //大区
	private String salesDepartMentName;   //分公司
	private String businessName;          //营业所
	private String businessNameCode;      //营业所SAP代码
	private String dealerName;            //经销商名称
	private String dealerNumber;          //经销商SAP代码
	private String cooperationStatus;     //合作状态
	private String Name;                  //姓名
	private String idCard;                //身份证号
	private String phoneNumber;           //手机号
	private String bankCardNumber;        //银行卡号
	private String Address;               //联系地址
	private String personnelStatus;       //人事状态
	private String accountStatus;         //账户状态
	private String personCreatTime;       //人员创建时间
	private String updateTime;            //数据更新时间 yyyy-mm-dd hh-mm-ss
	private String dataType;              //数据更新标记 1：新增数据，2：更新数据
	public String getTenantUserid() {
		return tenantUserid;
	}
	public void setTenantUserid(String tenantUserid) {
		this.tenantUserid = tenantUserid;
	}
	public String getRegionalName() {
		return regionalName;
	}
	public void setRegionalName(String regionalName) {
		this.regionalName = regionalName;
	}
	public String getSalesDepartMentName() {
		return salesDepartMentName;
	}
	public void setSalesDepartMentName(String salesDepartMentName) {
		this.salesDepartMentName = salesDepartMentName;
	}
	public String getBusinessName() {
		return businessName;
	}
	public void setBusinessName(String businessName) {
		this.businessName = businessName;
	}
	public String getBusinessNameCode() {
		return businessNameCode;
	}
	public void setBusinessNameCode(String businessNameCode) {
		this.businessNameCode = businessNameCode;
	}
	public String getDealerName() {
		return dealerName;
	}
	public void setDealerName(String dealerName) {
		this.dealerName = dealerName;
	}
	public String getDealerNumber() {
		return dealerNumber;
	}
	public void setDealerNumber(String dealerNumber) {
		this.dealerNumber = dealerNumber;
	}
	public String getCooperationStatus() {
		return cooperationStatus;
	}
	public void setCooperationStatus(String cooperationStatus) {
		this.cooperationStatus = cooperationStatus;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getIdCard() {
		return idCard;
	}
	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getBankCardNumber() {
		return bankCardNumber;
	}
	public void setBankCardNumber(String bankCardNumber) {
		this.bankCardNumber = bankCardNumber;
	}
	public String getAddress() {
		return Address;
	}
	public void setAddress(String address) {
		Address = address;
	}
	public String getPersonnelStatus() {
		return personnelStatus;
	}
	public void setPersonnelStatus(String personnelStatus) {
		this.personnelStatus = personnelStatus;
	}
	public String getAccountStatus() {
		return accountStatus;
	}
	public void setAccountStatus(String accountStatus) {
		this.accountStatus = accountStatus;
	}
	public String getPersonCreatTime() {
		return personCreatTime;
	}
	public void setPersonCreatTime(String personCreatTime) {
		this.personCreatTime = personCreatTime;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	@Override
	public String toString() {
		return "CustomerBusinessInfo [tenantUserid=" + tenantUserid + ", regionalName=" + regionalName
				+ ", salesDepartMentName=" + salesDepartMentName + ", businessName=" + businessName
				+ ", businessNameCode=" + businessNameCode + ", dealerName=" + dealerName + ", dealerNumber="
				+ dealerNumber + ", cooperationStatus=" + cooperationStatus + ", Name=" + Name + ", idCard=" + idCard
				+ ", phoneNumber=" + phoneNumber + ", bankCardNumber=" + bankCardNumber + ", Address=" + Address
				+ ", personnelStatus=" + personnelStatus + ", accountStatus=" + accountStatus + ", personCreatTime="
				+ personCreatTime + ", updateTime=" + updateTime + ", dataType=" + dataType + "]";
	}
}
