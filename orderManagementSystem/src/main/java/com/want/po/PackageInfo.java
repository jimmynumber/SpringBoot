package com.want.po;
import java.io.Serializable;


public class PackageInfo implements Serializable{

    private static final long serialVersionUID = 2120869894112984147L;

    private String packageId;
    private String packageName;
    private String prodSku;
    private String prodName;
    private Double prodCount;
    private Double salePrice;
    private Double discountPrice;
    private Double prodAmount;
    private String unitCode;
    private String unitName;
    private String saleCompany;
    private String distributionChannel;
    private String productGroup;
    private Double sumAmount;
    
	public String getPackageId() {
		return packageId;
	}
	public void setPackageId(String packageId) {
		this.packageId = packageId;
	}
	public String getPackageName() {
		return packageName;
	}
	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}
	public String getProdSku() {
		return prodSku;
	}
	public void setProdSku(String prodSku) {
		this.prodSku = prodSku;
	}
	public String getProdName() {
		return prodName;
	}
	public void setProdName(String prodName) {
		this.prodName = prodName;
	}
	public Double getProdCount() {
		return prodCount;
	}
	public void setProdCount(Double prodCount) {
		this.prodCount = prodCount;
	}
	public Double getSalePrice() {
		return salePrice;
	}
	public void setSalePrice(Double salePrice) {
		this.salePrice = salePrice;
	}
	public Double getDiscountPrice() {
		return discountPrice;
	}
	public void setDiscountPrice(Double discountPrice) {
		this.discountPrice = discountPrice;
	}
	public Double getProdAmount() {
		return prodAmount;
	}
	public void setProdAmount(Double prodAmount) {
		this.prodAmount = prodAmount;
	}
	public String getUnitCode() {
		return unitCode;
	}
	public void setUnitCode(String unitCode) {
		this.unitCode = unitCode;
	}
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public String getSaleCompany() {
		return saleCompany;
	}
	public void setSaleCompany(String saleCompany) {
		this.saleCompany = saleCompany;
	}
	public String getDistributionChannel() {
		return distributionChannel;
	}
	public void setDistributionChannel(String distributionChannel) {
		this.distributionChannel = distributionChannel;
	}
	public String getProductGroup() {
		return productGroup;
	}
	public void setProductGroup(String productGroup) {
		this.productGroup = productGroup;
	}
	public Double getSumAmount() {
		return sumAmount;
	}
	public void setSumAmount(Double sumAmount) {
		this.sumAmount = sumAmount;
	}
    
}