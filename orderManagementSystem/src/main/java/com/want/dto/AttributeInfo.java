package com.want.dto;

import java.io.Serializable;

public class AttributeInfo implements Serializable{

	private static final long serialVersionUID = 1497563631808187050L;
	private String attributeKey ;
	private String attributeValue ;
	private String attributeId ;
	private String attributeName;
	public String getAttributeKey() {
		return attributeKey;
	}
	public void setAttributeKey(String attributeKey) {
		this.attributeKey = attributeKey;
	}
	public String getAttributeValue() {
		return attributeValue;
	}
	public void setAttributeValue(String attributeValue) {
		this.attributeValue = attributeValue;
	}
	public String getAttributeId() {
		return attributeId;
	}
	public void setAttributeId(String attributeId) {
		this.attributeId = attributeId;
	}
	public String getAttributeName() {
		return attributeName;
	}
	public void setAttributeName(String attributeName) {
		this.attributeName = attributeName;
	}
	@Override
	public String toString() {
		return "AttributeInfo [attributeKey=" + attributeKey + ", attributeValue=" + attributeValue + ", attributeId="
				+ attributeId + ", attributeName=" + attributeName + "]";
	}
}
